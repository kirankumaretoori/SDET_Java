package lab5;

//1. Write a number to print the table of 5, first 10 values

public class PrintTableUsingForLoop {

	public static void main(String[] args) {

		System.out.println("######### Printing 5 table #########");

		for (int i = 1; i <= 10; i++)
			System.out.println(5 + " * " + i + " = " + (5 * i));

	}

}
