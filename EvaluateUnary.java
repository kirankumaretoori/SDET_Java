package lab1;

/* 2. Write a program where you evaluate the
result of an arithmetic expressions using
unary operators*/

public class EvaluateUnary {

	public static void main(String[] args) {

		// Declaring variables
		int num1 = 10, num2 = 20;

		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);

		// Adding & Assigning values
		num1 += num2;

		// Displaying the assigned values
		System.out.println("num1 = " + num1);

		// Subtracting & Assigning values
		num1 -= num2;

		// Displaying the assigned values
		System.out.println("num1 = " + num1);

		// Multiplying & Assigning values
		num1 *= num2;

		// Displaying the assigned values
		System.out.println("num1 = " + num1);

		// Dividing & Assigning values
		num1 /= num2;

		// Displaying the assigned values
		System.out.println("num1 = " + num1);

		// Moduling & Assigning values
		num1 %= num2;

		// Displaying the assigned values
		System.out.println("num1 = " + num1);
	}
}
